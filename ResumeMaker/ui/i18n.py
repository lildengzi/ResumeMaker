def _configured_language() -> str:
    configured = str(APP_CONFIG.get("ui", {}).get("language", DEFAULT_LANGUAGE)).lower()
    return configured if configured in LANGUAGE_OPTIONS else DEFAULT_LANGUAGE


def ensure_language_state() -> str:
    if "ui_language" not in st.session_state:
        st.session_state.ui_language = _configured_language()
    if st.session_state.ui_language not in LANGUAGE_OPTIONS:
        st.session_state.ui_language = DEFAULT_LANGUAGE
    return str(st.session_state.ui_language)


def current_language() -> str:
    return ensure_language_state()


def t(key: str, **kwargs: object) -> str:
    language = current_language()
    template = TEXT.get(language, TEXT[DEFAULT_LANGUAGE]).get(key)
    if template is None:
        template = TEXT[DEFAULT_LANGUAGE].get(key, key)
    return template.format(**kwargs) if kwargs else template


def render_language_selector() -> str:
    ensure_language_state()
    selected = st.selectbox(
        t("language.label"),
        options=list(LANGUAGE_OPTIONS),
        format_func=lambda code: t(f"language.{code}"),
        key="ui_language",
    )
    return str(selected)
